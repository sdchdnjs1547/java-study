package chap_02;

public class operater3 {
    //비교 연산
    public static void main(String[] args) {
        System.out.println(5>3);//5는 3보다 크다 t or f
        System.out.println(5<=3);// 5는 3보다 작거나 같다 f
        System.out.println(5>=5);// 5는 5보다 크고나 같다
        System.out.println(5==5);//5는  5와 같다(true
        System.out.println(5==3);//5는 3과 같다 false
        System.out.println(5!=5);//5는 5와 같지 않다 false
        System.out.println(5!=3);//5는 3과 같지 않다 true
    }
}
